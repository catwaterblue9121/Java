public class Cadeira {

    private String tipo;
    private String cor;
    private String apoiadores;
    private String almofada;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getApoiadores() {
        return apoiadores;
    }

    public void setApoiadores(String apoiadores) {
        this.apoiadores = apoiadores;
    }

    public String getAlmofada() {
        return almofada;
    }

    public void setAlmofada(String almofada) {
        this.almofada = almofada;
    }
}
